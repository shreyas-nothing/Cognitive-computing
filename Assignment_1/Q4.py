# Q.4.1 Write a program to print numbers from 1 to 10 using a for loop.
for i in range(1,11):
    print(i)

print('\n')
# Q.4.2 Write a program to print numbers from 1 to 10 using a while loop.
num = 1
while num<11:
    print(num)
    num += 1

print('\n')
# Q.4.3 Write a program to calculate the sum of numbers from 1 to 100 using a loop.
sum = 0
for x in range(1,101):
    sum += x
print(sum)